    /**
     * Generate R code for metafor package
     */
    function generateRCode(data, settings, results) {
        const { analysisType, effectMeasure, tau2Method, hksjAdjustment } = settings;

        let code = `# PrognosisMeta - Generated R Code
# Meta-analysis using metafor package
# Generated: ${new Date().toISOString()}

# Install required packages if needed
# install.packages("metafor")

library(metafor)

# Study data
study <- c(${data.map(d => `"${String(d.study || '').replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n')}"`).join(', ')})
`;

        // Add effect-specific data
        if (['HR', 'OR', 'RR'].includes(effectMeasure)) {
            code += `yi <- c(${data.map(d => d.effect.toFixed(6)).join(', ')})  # Log-transformed effects
sei <- c(${data.map(d => d.se.toFixed(6)).join(', ')})  # Standard errors
vi <- sei^2  # Variances

# Create data frame
dat <- data.frame(study = study, yi = yi, sei = sei, vi = vi)
`;
        } else if (effectMeasure === 'cstat') {
            code += `cstat <- c(${data.map(d => d.originalEffect.toFixed(4)).join(', ')})
se <- c(${data.map(d => d.originalSE?.toFixed(4) || 'NA').join(', ')})

# Logit transformation for C-statistic
yi <- log(cstat / (1 - cstat))
sei <- se / (cstat * (1 - cstat))
vi <- sei^2

dat <- data.frame(study = study, cstat = cstat, yi = yi, sei = sei, vi = vi)
`;
        } else {
            code += `yi <- c(${data.map(d => d.effect.toFixed(6)).join(', ')})
sei <- c(${data.map(d => d.se.toFixed(6)).join(', ')})
vi <- sei^2

dat <- data.frame(study = study, yi = yi, sei = sei, vi = vi)
`;
        }

        // Main meta-analysis
        const method = tau2Method === 'DL' ? 'DL' :
                       tau2Method === 'REML' ? 'REML' :
                       tau2Method === 'PM' ? 'PM' :
                       tau2Method === 'ML' ? 'ML' : 'REML';

        code += `
# Random-effects meta-analysis
res <- rma(yi = yi, vi = vi, data = dat, method = "${method}"${hksjAdjustment ? ', test = "knha"' : ''})

# Summary
summary(res)

# Forest plot
forest(res, slab = dat$study, xlab = "${getEffectLabel(effectMeasure)}",
       ${['HR', 'OR', 'RR'].includes(effectMeasure) ? 'atransf = exp,' : ''}
       header = "Study")
`;

        // Back-transformation for C-statistic
        if (effectMeasure === 'cstat') {
            code += `
# Back-transform to C-statistic scale
pooled_cstat <- plogis(res$beta)
ci_lower <- plogis(res$ci.lb)
ci_upper <- plogis(res$ci.ub)
cat("Pooled C-statistic:", round(pooled_cstat, 3),
    "[", round(ci_lower, 3), ",", round(ci_upper, 3), "]\\n")
`;
        }

        // Heterogeneity
        code += `
# Heterogeneity statistics
cat("\\nHeterogeneity:\\n")
cat("tau^2 =", round(res$tau2, 4), "\\n")
cat("I^2 =", round(res$I2, 1), "%\\n")
cat("H^2 =", round(res$H2, 2), "\\n")
cat("Q =", round(res$QE, 2), ", df =", res$k - 1, ", p =", round(res$QEp, 4), "\\n")
`;

        // Publication bias
        code += `
# Publication bias tests
# Funnel plot
funnel(res, xlab = "${getEffectLabel(effectMeasure)}")

# Egger's test
regtest(res)

# Trim and fill
tf <- trimfill(res)
summary(tf)
funnel(tf)

# Begg's test (rank correlation)
ranktest(res)
`;

        // Prediction interval
        code += `
# Prediction interval
predict(res)
`;

        // Subgroup analysis placeholder
        if (data.some(d => d.subgroup)) {
            code += `
# Subgroup analysis
dat$subgroup <- c(${data.map(d => d.subgroup ? `"${String(d.subgroup).replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n')}"` : 'NA').join(', ')})
res_sub <- rma(yi = yi, vi = vi, mods = ~ subgroup, data = dat, method = "${method}")
summary(res_sub)
`;
        }

        code += `
# Leave-one-out analysis
loo <- leave1out(res)
print(loo)

# Influence diagnostics
inf <- influence(res)
plot(inf)
`;

        return code;
    }

    /**
     * Generate Stata code
     */
    function generateStataCode(data, settings) {
        const { effectMeasure, tau2Method } = settings;

        let code = `* PrognosisMeta - Generated Stata Code
* Meta-analysis
* Generated: ${new Date().toISOString()}

* Clear and set up
clear all
set more off

* Input data
input str50 study yi sei
`;

        data.forEach(d => {
            // Escape quotes and newlines for Stata string syntax
            const escapedStudy = String(d.study || '').replace(/"/g, '""').replace(/[\n\r]/g, ' ');
            code += `"${escapedStudy}" ${d.effect.toFixed(6)} ${d.se.toFixed(6)}\n`;
        });

        code += `end

* Generate variance
gen vi = sei^2

* Random-effects meta-analysis
metan yi sei, random${tau2Method === 'REML' ? ' reml' : ''} ///
    label(namevar=study) ///
    effect(${getEffectLabel(effectMeasure)}) ///
    ${['HR', 'OR', 'RR'].includes(effectMeasure) ? 'eform' : ''} ///
    forestplot

* Heterogeneity (already reported by metan)

* Funnel plot
metafunnel yi sei, ///
    xtitle(${getEffectLabel(effectMeasure)}) ///
    ${['HR', 'OR', 'RR'].includes(effectMeasure) ? 'eform' : ''}

* Publication bias tests
* Egger's test
metabias yi sei, egger

* Begg's test
metabias yi sei, begg

* Trim and fill
metatrim yi sei

* Influence analysis
metainf yi sei, random
`;

        return code;
    }

    /**
     * Generate Python code
     */
    function generatePythonCode(data, settings) {
        const { effectMeasure, tau2Method } = settings;

        let code = `# PrognosisMeta - Generated Python Code
# Meta-analysis using statsmodels
# Generated: ${new Date().toISOString()}

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from scipy import stats

# Try to import optional packages
try:
    from statsmodels.stats.meta_analysis import CombineResults
    HAS_STATSMODELS = True
except ImportError:
    HAS_STATSMODELS = False
    print("statsmodels not available - using basic implementation")

# Study data
data = pd.DataFrame({
    'study': [${data.map(d => `'${String(d.study || '').replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`).join(', ')}],
    'yi': [${data.map(d => d.effect.toFixed(6)).join(', ')}],
    'sei': [${data.map(d => d.se.toFixed(6)).join(', ')}]
})
data['vi'] = data['sei'] ** 2
data['wi'] = 1 / data['vi']

print("Study data:")
print(data)
print()

# Fixed-effect meta-analysis
w_sum = data['wi'].sum()
effect_fe = (data['wi'] * data['yi']).sum() / w_sum
se_fe = np.sqrt(1 / w_sum)
z_fe = effect_fe / se_fe
p_fe = 2 * (1 - stats.norm.cdf(abs(z_fe)))

print(f"Fixed-Effect Results:")
print(f"  Effect: {effect_fe:.4f}")
print(f"  SE: {se_fe:.4f}")
print(f"  95% CI: [{effect_fe - 1.96*se_fe:.4f}, {effect_fe + 1.96*se_fe:.4f}]")
print(f"  z = {z_fe:.2f}, p = {p_fe:.4f}")
print()

# Cochran's Q and I²
Q = ((data['wi'] * (data['yi'] - effect_fe)**2)).sum()
df = len(data) - 1
p_het = 1 - stats.chi2.cdf(Q, df)
C = w_sum - (data['wi']**2).sum() / w_sum
tau2 = max(0, (Q - df) / C)  # DerSimonian-Laird
I2 = max(0, 100 * (Q - df) / Q) if Q > df else 0

print(f"Heterogeneity:")
print(f"  Q = {Q:.2f}, df = {df}, p = {p_het:.4f}")
print(f"  tau² = {tau2:.4f}")
print(f"  I² = {I2:.1f}%")
print()

# Random-effects meta-analysis
data['wi_re'] = 1 / (data['vi'] + tau2)
w_sum_re = data['wi_re'].sum()
effect_re = (data['wi_re'] * data['yi']).sum() / w_sum_re
se_re = np.sqrt(1 / w_sum_re)
z_re = effect_re / se_re
p_re = 2 * (1 - stats.norm.cdf(abs(z_re)))

print(f"Random-Effects Results (DL):")
print(f"  Effect: {effect_re:.4f}")
print(f"  SE: {se_re:.4f}")
print(f"  95% CI: [{effect_re - 1.96*se_re:.4f}, {effect_re + 1.96*se_re:.4f}]")
print(f"  z = {z_re:.2f}, p = {p_re:.4f}")
`;

        if (['HR', 'OR', 'RR'].includes(effectMeasure)) {
            code += `
# Back-transform to original scale
effect_exp = np.exp(effect_re)
ci_lower = np.exp(effect_re - 1.96 * se_re)
ci_upper = np.exp(effect_re + 1.96 * se_re)
print(f"\\n${effectMeasure}: {effect_exp:.2f} [{ci_lower:.2f}, {ci_upper:.2f}]")
`;
        }

        code += `
# Prediction interval
k = len(data)
t_crit = stats.t.ppf(0.975, k - 2)
pi_var = 1/w_sum_re + tau2
pi_lower = effect_re - t_crit * np.sqrt(pi_var)
pi_upper = effect_re + t_crit * np.sqrt(pi_var)
print(f"\\n95% Prediction Interval: [{pi_lower:.4f}, {pi_upper:.4f}]")

# Forest plot
fig, ax = plt.subplots(figsize=(10, 6))

# Study points and CIs
y_pos = range(len(data))
for i, (_, row) in enumerate(data.iterrows()):
    lower = row['yi'] - 1.96 * row['sei']
    upper = row['yi'] + 1.96 * row['sei']
    ax.errorbar(row['yi'], i, xerr=[[row['yi'] - lower], [upper - row['yi']]],
                fmt='s', color='blue', capsize=3, markersize=np.sqrt(row['wi_re'] / w_sum_re) * 20)

# Pooled diamond
ax.fill([effect_re - 1.96*se_re, effect_re, effect_re + 1.96*se_re, effect_re],
        [len(data) + 0.5, len(data) + 0.8, len(data) + 0.5, len(data) + 0.2],
        color='red', alpha=0.7)

# Reference line
ax.axvline(x=0, color='gray', linestyle='--', alpha=0.5)

ax.set_yticks(list(y_pos) + [len(data) + 0.5])
ax.set_yticklabels(list(data['study']) + ['Pooled'])
ax.set_xlabel('Effect Size')
ax.set_title('Forest Plot')
plt.tight_layout()
plt.savefig('forest_plot.png', dpi=300)
plt.show()

# Funnel plot
fig, ax = plt.subplots(figsize=(8, 6))
ax.scatter(data['yi'], data['sei'], s=50, alpha=0.7)
ax.axvline(x=effect_re, color='red', linestyle='-', label='Pooled effect')

# Pseudo 95% CI lines
max_se = data['sei'].max() * 1.1
se_range = np.linspace(0, max_se, 100)
ax.plot(effect_re - 1.96 * se_range, se_range, 'k--', alpha=0.5)
ax.plot(effect_re + 1.96 * se_range, se_range, 'k--', alpha=0.5)

ax.invert_yaxis()
ax.set_xlabel('Effect Size')
ax.set_ylabel('Standard Error')
ax.set_title('Funnel Plot')
plt.tight_layout()
plt.savefig('funnel_plot.png', dpi=300)
plt.show()

# Egger's test
precision = 1 / data['sei']
standardized = data['yi'] / data['sei']
slope, intercept, r_value, p_value, std_err = stats.linregress(precision, standardized)
print(f"\\nEgger's test:")
print(f"  Intercept = {intercept:.3f}")
print(f"  p-value = {p_value:.4f}")
`;

        return code;
    }

