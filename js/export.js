    /**
     * Export data to CSV format
     */
    function toCSV(data, options = {}) {
        if (!data) return '';

        const { delimiter = ',', includeHeaders = true } = options;
        let csv = '';

        // Handle array of objects
        if (Array.isArray(data)) {
            if (data.length === 0) return '';

            const keys = Object.keys(data[0]);
            if (includeHeaders) {
                csv += keys.join(delimiter) + '\n';
            }

            data.forEach(row => {
                csv += keys.map(key => {
                    let val = row[key];
                    if (val === null || val === undefined) return '';
                    let strVal = String(val);
                    // Guard against CSV formula injection
                    if (/^[=+\-@\t\r]/.test(strVal)) {
                        strVal = "'" + strVal;
                    }
                    if (strVal.includes(delimiter) || strVal.includes('"') || strVal.includes('\n')) {
                        return '"' + strVal.replace(/"/g, '""') + '"';
                    }
                    return strVal;
                }).join(delimiter) + '\n';
            });
        }
        // Handle single object
        else if (typeof data === 'object') {
            Object.entries(data).forEach(([key, value]) => {
                if (typeof value === 'object' && value !== null) {
                    csv += key + '\n';
                    csv += toCSV(Array.isArray(value) ? value : [value], options);
                } else {
                    csv += key + delimiter + String(value ?? '') + '\n';
                }
            });
        }

        return csv;
    }

    /**
     * Export data to JSON format
     */
    function toJSON(data, options = {}) {
        const { pretty = true, indent = 2 } = options;

        try {
            if (pretty) {
                return JSON.stringify(data, null, indent);
            }
            return JSON.stringify(data);
        } catch (e) {
            console.error('JSON export error:', e);
            return null;
        }
    }

    /**
     * Generate a complete analysis report
     */
    function generateReport(analysisData, format = 'html', options = {}) {
        switch (format.toLowerCase()) {
            case 'html':
                return generateHTMLReport(analysisData, options);
            case 'csv':
                return exportResultsCSV(
                    analysisData.results,
                    analysisData.data,
                    analysisData.settings
                );
            case 'json':
                return toJSON(analysisData, options);
            case 'r':
                return generateRCode(
                    analysisData.data,
                    analysisData.settings,
                    analysisData.results
                );
            case 'stata':
                return generateStataCode(
                    analysisData.data,
                    analysisData.settings
                );
            case 'python':
                return generatePythonCode(
                    analysisData.data,
                    analysisData.settings
                );
            default:
                return generateHTMLReport(analysisData, options);
        }
    }

