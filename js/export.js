    function formatEffect(effect, settings) {
        if (effect === undefined || effect === null || isNaN(effect) || !isFinite(effect)) return '--';

        const em = settings?.effectMeasure || 'HR';

        if (['HR', 'OR', 'RR'].includes(em)) {
            const exp = Math.exp(effect);
            return isFinite(exp) ? exp.toFixed(2) : '--';
        } else if (em === 'cstat') {
            return (1 / (1 + Math.exp(-effect))).toFixed(3);  // Inverse logit
        } else {
            return effect.toFixed(3);
        }
    }

    function formatCI(ci, settings) {
        if (!ci) return '[-- , --]';
        if (ci.lower === undefined || ci.upper === undefined) return '[-- , --]';
        if (!isFinite(ci.lower) || !isFinite(ci.upper)) return '[-- , --]';

        const em = settings?.effectMeasure || 'HR';

        if (['HR', 'OR', 'RR'].includes(em)) {
            return `[${Math.exp(ci.lower).toFixed(2)}, ${Math.exp(ci.upper).toFixed(2)}]`;
        } else if (em === 'cstat') {
            return `[${(1 / (1 + Math.exp(-ci.lower))).toFixed(3)}, ${(1 / (1 + Math.exp(-ci.upper))).toFixed(3)}]`;
        } else {
            return `[${ci.lower.toFixed(3)}, ${ci.upper.toFixed(3)}]`;
        }
    }

    function formatPValue(p) {
        if (p === undefined || p === null || isNaN(p)) return '= --';
        if (p < 0.001) return '< 0.001';
        return `= ${p.toFixed(3)}`;
    }

    function interpretI2(i2) {
        if (i2 === undefined || i2 === null) return '';
        if (i2 < 25) return 'Low heterogeneity';
        if (i2 < 50) return 'Moderate heterogeneity';
        if (i2 < 75) return 'Substantial heterogeneity';
        return 'Considerable heterogeneity';
    }

