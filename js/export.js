    function exportResultsCSV(results, data, settings) {
        let csv = 'Meta-Analysis Results\n\n';

        csv += 'SUMMARY\n';
        csv += `Analysis Type,${settings.analysisType}\n`;
        csv += `Effect Measure,${settings.effectMeasure}\n`;
        csv += `Number of Studies,${data.length}\n`;
        csv += `Method,${results.method}\n\n`;

        csv += 'POOLED ESTIMATE\n';
        csv += `Effect,${formatEffect(results.effect, settings)}\n`;
        csv += `Lower CI,${formatEffect(results.ci?.lower, settings)}\n`;
        csv += `Upper CI,${formatEffect(results.ci?.upper, settings)}\n`;
        csv += `SE,${results.se?.toFixed(4)}\n`;
        csv += `z/t value,${(results.zValue || results.tValue)?.toFixed(2)}\n`;
        csv += `p-value,${results.pValue?.toFixed(4)}\n\n`;

        csv += 'HETEROGENEITY\n';
        csv += `Q,${results.Q?.toFixed(2)}\n`;
        csv += `df,${results.df}\n`;
        csv += `p (heterogeneity),${results.pHet?.toFixed(4)}\n`;
        csv += `I²,${results.I2?.toFixed(1)}%\n`;
        csv += `tau²,${results.tau2?.toFixed(4)}\n`;
        csv += `tau,${results.tau?.toFixed(4)}\n\n`;

        csv += 'STUDY DETAILS\n';
        csv += 'Study,N,Effect,SE,Lower CI,Upper CI,Weight (%)\n';
        data.forEach((study, i) => {
            // Escape quotes and guard against CSV formula injection
            let escapedStudy = String(study.study || '').replace(/"/g, '""');
            // Prefix with single quote if starts with formula characters
            if (/^[=+\-@\t\r]/.test(escapedStudy)) {
                escapedStudy = "'" + escapedStudy;
            }
            csv += `"${escapedStudy}",${study.n || ''},${formatEffect(study.effect, settings)},`;
            csv += `${study.se?.toFixed(4)},${formatEffect(study.effect - 1.96 * study.se, settings)},`;
            csv += `${formatEffect(study.effect + 1.96 * study.se, settings)},${results.weights?.[i]?.toFixed(1) || ''}\n`;
        });

        return csv;
    }


